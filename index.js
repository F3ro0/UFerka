let countEl1 = document.getElementById("count-el1")

let count1 = 0;

function increment1(){
  count1 += 1
  countEl1.textContent = count1
};

function decrement1() {if (count1 === 0) {
  alert("You can't go to negative values!")
} else {
  count1 -= 1
  countEl1.textContent = count1
};
};


